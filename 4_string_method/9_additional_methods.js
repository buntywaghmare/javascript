// 9. Additional Useful Methods

// padStart() and padEnd()
console.log('5'.padStart(3, '0')); // Output: '005'
console.log('5'.padEnd(3, '0')); // Output: '500'

// repeat()
console.log('Ha'.repeat(3)); // Output: 'HaHaHa'

// charAt() vs []
console.log('Hello'.charAt(1)); // Output: 'e'
console.log('Hello'[1]); // Output: 'e'